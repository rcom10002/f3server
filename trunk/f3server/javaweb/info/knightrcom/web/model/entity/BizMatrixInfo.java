package info.knightrcom.web.model.entity;


public class BizMatrixInfo {
	private String userId;
	
	private String totalRechargeScore;
	
	private String totalGameScore;
	
	private String totalSysScore;
	
	private String currentScore;

	/**
	 * @return the userId
	 */
	public String getUserId() {
		return userId;
	}

	/**
	 * @param userId the userId to set
	 */
	public void setUserId(String userId) {
		this.userId = userId;
	}

	/**
	 * @return the totalRechargeScore
	 */
	public String getTotalRechargeScore() {
		return totalRechargeScore;
	}

	/**
	 * @param totalRechargeScore the totalRechargeScore to set
	 */
	public void setTotalRechargeScore(String totalRechargeScore) {
		this.totalRechargeScore = totalRechargeScore;
	}

	/**
	 * @return the totalGameScore
	 */
	public String getTotalGameScore() {
		return totalGameScore;
	}

	/**
	 * @param totalGameScore the totalGameScore to set
	 */
	public void setTotalGameScore(String totalGameScore) {
		this.totalGameScore = totalGameScore;
	}

	/**
	 * @return the totalSysScore
	 */
	public String getTotalSysScore() {
		return totalSysScore;
	}

	/**
	 * @param totalSysScore the totalSysScore to set
	 */
	public void setTotalSysScore(String totalSysScore) {
		this.totalSysScore = totalSysScore;
	}

	/**
	 * @return the currentScore
	 */
	public String getCurrentScore() {
		return currentScore;
	}

	/**
	 * @param currentScore the currentScore to set
	 */
	public void setCurrentScore(String currentScore) {
		this.currentScore = currentScore;
	}
	
	
}
